#Variables ideal mente que sea verboso que explique con solo leerlo
usuario1 = "Pepito"
usuario2 = "Maria"
usuario3 = "Alan"

#Concatenar = unir
print("El primer usuario es " + usuario1 + "y el sengudo usuario es " + usuario2 + "y el tercer usuario es " + usuario3)

#Forma mas elegante
#Interpolacion es imprimir el valor de una variables dentro de una cadena de texto
#f habilita las funciones f= funciones
print(f"El primer usuario es {usuario1} el segundo usuario es {usuario2} y el tercer usuario es {usuario3}")
