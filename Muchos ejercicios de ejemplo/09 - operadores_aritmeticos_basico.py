# Python puede funcionar como una calculadora.
# Estos son los 7 operadores aritméticos fundamentales.

a = 15
b = 4

print(f"a = {a}")
print(f"b = {b}")
print("-" * 30)

print(f"Suma:            a + b = {a + b}")       # 19
print(f"Resta:           a - b = {a - b}")       # 11
print(f"Multiplicación:  a * b = {a * b}")       # 60
print(f"División:        a / b = {a / b}")       # 3.75  (siempre retorna float)
print(f"División entera: a // b = {a // b}")     # 3     (solo la parte entera)
print(f"Módulo (resto):  a % b = {a % b}")       # 3     (el resto de dividir)
print(f"Potencia:        a ** b = {a ** b}")     # 50625 (15 elevado a 4)
