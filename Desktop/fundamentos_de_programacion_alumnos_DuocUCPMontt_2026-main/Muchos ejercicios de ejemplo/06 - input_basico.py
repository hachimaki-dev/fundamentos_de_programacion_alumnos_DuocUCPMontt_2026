# 06 - ENTRADA DE DATOS (BÁSICO)
# Tema: Leer datos del usuario con input()
# Nivel: ⭐ Básico
# ============================================
# input() pausa el programa y espera que el usuario escriba algo.
# Siempre devuelve un texto (str), incluso si el usuario escribe un número.

nombre = input("¿Cómo te llamas? ")
print("¡Hola,", nombre + "!")

carrera = input("¿Qué carrera estudias? ")
print("Excelente, estás estudiando", carrera)

color = input("¿Cuál es tu color favorito? ")
print("A mí también me gusta el", color)
