import random

# Entero entre 1 y 100
random.randint(1, 100)

# Decimal entre 0.0 y 1.0
random.random()

# Decimal entre dos valores
random.uniform(1.5, 9.5)

# Elemento aleatorio de una lista
random.choice(["rojo", "azul", "verde"])

# Barajar una lista
lista = [1, 2, 3, 4, 5]
random.shuffle(lista)

