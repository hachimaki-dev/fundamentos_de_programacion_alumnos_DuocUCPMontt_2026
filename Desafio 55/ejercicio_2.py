#  Proyección de Ahorro para un Auto

costo_del_auto = 5000000
ahorros = 1500000
dinero = costo_del_auto - ahorros # El dinero faltante para comprar el auto
print('Dinero faltante:', dinero)
meses_completados = dinero // 150000 # la cantidad de meses para tener la plata ahorrada
print('Esta cantidad de meses me falta para comprar el auto: ',meses_completados)