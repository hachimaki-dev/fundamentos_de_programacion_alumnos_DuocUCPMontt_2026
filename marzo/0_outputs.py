# En Python, se pueden hacer comentarios con "#"
print("Hello World")

# Los print pueden concatenar
print("Hola " + ":D")

usuario1 = "Pepito"
usuario2 = "Maria"
usuario3 = "Alan"

print("El primer usuario es " + usuario1 + ", el segundo usuario es " + usuario2 + " y el tercer usuario es " + usuario3 + ".")

# Forma más elegante: INTERPOLACIÓN
# Permite imprimir el valor de una variable dentro de una cadena de texto
print(f"El primer usuario es {usuario1}, el segundo usuario es {usuario2} y el tercer usuario es {usuario3}.")
