# Variables
usuario1 = "Pepito"
usuario2 = "Maria"
usuario3 = "Alan"

print("El primer usuario es " + usuario1 + " y el segundo usuario es " + usuario2 + " y el tercer usuario es " + usuario3)

# Forma mas elegante
# f: interpolacion, imprimir valor de una variable dentro de una cadena de texto
print(f"El primer usuario es {usuario1} y el segundo usuario es {usuario2} y el tercer usuario es {usuario3}")
